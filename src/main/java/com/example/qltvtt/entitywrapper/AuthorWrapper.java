package com.example.qltvtt.entitywrapper;

public class AuthorWrapper {
    private int Id;
    private String name;
    private String note;

    public int getId() {
        return Id;
    }

    public void setId(Integer Id) {
        this.Id = Id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
